"""File-based repositories."""


class PyFileWordRepository:
    def __init__(self, words):
        self.words = words


class PyFileVerbRepository:
    def __init__(self, verbs):
        self.verbs = verbs


class PyFileTextRepository:
    def __init__(self, texts):
        self.texts = texts


class PyFileSentenceRepository:
    def __init__(self, sentences):
        self.sentences = sentences

